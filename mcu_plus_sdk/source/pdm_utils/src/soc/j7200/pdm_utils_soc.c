/* This returns base address of LPSCs for the given PSC */
uint32_t PdmUtils_getLpscBaseAddr(uint32_t pscIndex)
{
   uint32_t baseAddr;

   if(J7200_PSC_INST_J7_WKUP_PSC_WRAP_WKUP_0 == pscIndex)
   {
       baseAddr = PDM_UTILS_PSC_WKUP_BASE_ADDR;
   }
   else if(J7200_PSC_INST_J7_MAIN_PSC_WRAP_MAIN_0 == pscIndex)
   {
       baseAddr = PDM_UTILS_PSC_MAIN_BASE_ADDR;
   }
   else
   {
       baseAddr = PDM_UTILS_INVALID_VAL;
   }

   return baseAddr;
}

