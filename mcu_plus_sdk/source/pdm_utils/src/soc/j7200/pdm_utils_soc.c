/* This API populates the given pointer to data object with number of available PSCs and PDs of
 * the device */
int32_t PdmUtils_getNumPsc(void *buffer, uint32_t size)
{

    int32_t ret = PDM_UTILS_PASS;
    PdmUtils_PscDataBook *pscDatabook;
    uint32_t pscIdx=0U;

    if((size < sizeof(PdmUtils_PscDataBook)) || (NULL == buffer))
    {
        ret = PDM_UTILS_EFAIL;
    }
    else
    {
        pscDatabook = (PdmUtils_PscDataBook*)buffer;
        pscDatabook->numPsc = PDM_UTILS_NUM_MAX_PSC;
        for(pscIdx=0U; pscIdx<pscDatabook->numPsc; pscIdx++)
        {
            if(pscIdx == J7200_PSC_INST_J7_WKUP_PSC_WRAP_WKUP_0)
            {
                pscDatabook->psc[pscIdx].numPD = PDM_UTILS_NUM_MAX_PD_MCU;
                pscDatabook->psc[pscIdx].pd[0].numLpsc = PDM_UTILS_NUM_MAX_LPSC_MCU;
            }
            else if(pscIdx == J7200_PSC_INST_J7_MAIN_PSC_WRAP_MAIN_0)
            {
                pscDatabook->psc[pscIdx].numPD = PDM_UTILS_NUM_MAX_PD_MAIN;
                pscDatabook->psc[pscIdx].pd[0].numLpsc = PDM_UTILS_NUM_MAX_LPSC_MAIN;
            }
        }
    }

    return ret;
}
