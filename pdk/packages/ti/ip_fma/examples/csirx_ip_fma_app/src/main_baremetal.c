#if defined(SOC_J721S2)
#include <ti/drv/sciclient/soc/sysfw/include/j721s2/tisci_devices.h>
#elif defined(SOC_J784S4)
#include <ti/drv/sciclient/soc/sysfw/include/j784s4/tisci_devices.h>
#endif
